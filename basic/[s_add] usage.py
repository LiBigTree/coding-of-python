# 1、 ctrl + / 批量注释

# 2、鼠标放在函数上，按CTRL+b可以看说明文档

# 3、可以通过快捷键Alt+Insert来快速创建一些方法

# 4、可以使用快捷键Ctrl+Alt+T来对已有的代码进行操作，比如添加if语句，异常等。

# 5、可以在当前文件中搜索相关内容，可以使用快捷键Ctrl+F,想在整个项目搜索相关内容可以使用Ctrl+Shift+F。
#
# 6、查找某一内容在哪里被使用到：可以使用快捷键Alt+F7,PyCharm会搜素并列出使用这一内容的地方
#
# 7、查找最近打开的文件：可以使用快捷键Ctrl+E将会调出最近文件的窗口，可以快速选择你要切换的文件。
#
# 8、搜索快捷键：可以使用Ctrl+Shift+A,然后搜索快捷键的名称，就可以知道使用什么快捷键了。

# 9、在所有地方查找：双击 shift 键就可以查找所有地方的文件！

